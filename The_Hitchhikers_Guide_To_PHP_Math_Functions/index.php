<?php
// I am sorry Douglas Adams. 
$initial = '555';
//To dec number
$a = octdec($initial);

echo "$a \n";
//degrees to Radian. 
$b = deg2rad($a);

echo "$b \n";

//Cosine of $b
$c = cos($b);

echo "$c \n";

//Round $c to 3 decimal places
$d = round($c, 3);

echo "$d \n";

//Natural log of $D
$e = log($d);

echo "$e \n";

//Absolute of $e
$f = abs($e);

echo "$f \n";